def run_single_horizon(h, feature_columns, target_col, aux_col,
                       df_train_n, df_val_n, df_test_n, norm_params,
                       device, label=""):
    """Entrena y evalua un modelo con un horizonte dado. Devuelve dict de resultados."""

    print(f"\n{'='*60}")
    print(f"  HORIZON = {h} ({h*5} min) {label}")
    print(f"{'='*60}")

    # Crear DataLoaders
    train_l, val_l, test_l = create_dataloaders(
        df_train_n, df_val_n, df_test_n,
        feature_columns, target_col, aux_col,
        seq_length=72, horizon=h, batch_size=256,
    )

    # Modelo
    model = TwoStageTCN(n_features=len(feature_columns))
    model = model.to(device)

    # Loss
    criterion = TwoStageLoss(
        event_threshold=0.5,
        norm_params=norm_params,
        cls_weight=0.3,
        reg_weight=0.7,
    )

    # Config
    config = {
        'learning_rate': 5e-4,
        'weight_decay': 1e-4,
        'max_epochs': 100,
        'min_epochs': 0,
        'grad_clip_max_norm': 1.0,
        'early_stopping_patience': 10,
        'early_stopping_min_delta': 1e-5,
    }

    # Entrenar
    history = train_model(model, train_l, val_l, criterion, config)

    # Evaluar
    y_pred, y_real = predict(model, test_l, device)

    tc = norm_params['target_col']
    tm = norm_params['mean'][tc]
    ts = norm_params['std'][tc]

    y_real_mgd = np.clip(np.expm1(y_real * ts + tm), 0, None)
    y_pred_mgd = np.clip(np.expm1(y_pred * ts + tm), 0, None)

    # Metricas
    ss_res = np.sum((y_real_mgd - y_pred_mgd)**2)
    ss_tot = np.sum((y_real_mgd - y_real_mgd.mean())**2)
    nse = 1 - ss_res / ss_tot
    rmse = np.sqrt(np.mean((y_real_mgd - y_pred_mgd)**2))
    mae = np.mean(np.abs(y_real_mgd - y_pred_mgd))

    peak_idx = np.argmax(y_real_mgd)
    peak_real = y_real_mgd[peak_idx]
    peak_pred = y_pred_mgd[peak_idx]
    err_pico = (peak_pred - peak_real) / peak_real * 100

    ext_mask = y_real_mgd > 50
    bias_ext = np.mean(y_pred_mgd[ext_mask] - y_real_mgd[ext_mask]) if ext_mask.sum() > 0 else float('nan')
    base_mask = y_real_mgd < 0.5
    bias_base = np.mean(y_pred_mgd[base_mask] - y_real_mgd[base_mask]) if base_mask.sum() > 0 else float('nan')

    result = {
        'horizon': h, 'minutes': h*5,
        'best_epoch': history['best_epoch'],
        'nse': nse, 'rmse': rmse, 'mae': mae,
        'err_pico': err_pico, 'bias_base': bias_base,
        'bias_extreme': bias_ext,
        'peak_real': peak_real, 'peak_pred': peak_pred,
    }

    print(f"\n  H={h} ({h*5}min): NSE={nse:.3f} | RMSE={rmse:.2f} | "
          f"Pico: {peak_real:.1f}->{peak_pred:.1f} ({err_pico:+.1f}%) | "
          f"Bias extr={bias_ext:+.1f}")

    # ─── Plots ────────────────────────────────────────────
    matplotlib.use('module://matplotlib_inline.backend_inline')
    import matplotlib.pyplot as plt

    # Scatter
    fig, ax = plt.subplots(figsize=(7, 7))
    n = len(y_real_mgd)
    idx = np.random.choice(n, min(n, 50000), replace=False)
    ax.scatter(y_real_mgd[idx], y_pred_mgd[idx], s=2, alpha=0.1, color='darkorange')
    mx = max(y_real_mgd.max(), y_pred_mgd.max())
    ax.plot([0, mx], [0, mx], 'k--', lw=1)
    ax.set_xlabel('Real (MGD)')
    ax.set_ylabel('Predicho (MGD)')
    ax.set_title(f'H={h} ({h*5}min) {label} — NSE={nse:.3f}', fontweight='bold')
    ax.set_aspect('equal')
    plt.tight_layout()
    plt.show()

    # Top 3 picos
    top3 = np.argsort(y_real_mgd)[-3:][::-1]
    for rank, pidx in enumerate(top3, 1):
        w = 500
        s = max(0, pidx - w)
        e = min(len(y_real_mgd), pidx + w)
        fig, ax = plt.subplots(figsize=(14, 3))
        x_ax = np.arange(s, e)
        ax.plot(x_ax, y_real_mgd[s:e], color='black', lw=1, label='Real')
        ax.plot(x_ax, y_pred_mgd[s:e], color='darkorange', lw=1, ls='--', label='Predicho')
        ax.fill_between(x_ax, y_real_mgd[s:e], y_pred_mgd[s:e], alpha=0.15, color='red')
        pr = y_real_mgd[pidx]
        pp = y_pred_mgd[pidx]
        er = (pp - pr) / pr * 100
        ax.set_title(f'H={h} {label} — Pico #{rank}: {pr:.1f}->{pp:.1f} MGD ({er:+.0f}%)', fontweight='bold')
        ax.set_xlabel('Indice temporal (5 min)')
        ax.set_ylabel('Stormflow (MGD)')
        ax.legend()
        plt.tight_layout()
        plt.show()

    plt.close('all')

    # Liberar
    del model, criterion, train_l, val_l, test_l, y_pred, y_real
    del y_pred_mgd, y_real_mgd
    torch.cuda.empty_cache()
    gc.collect()

    return result


