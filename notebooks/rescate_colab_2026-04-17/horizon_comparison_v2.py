for h in horizons:
    print(f"\n{'='*60}")
    print(f"  HORIZON = {h} ({h*5} min) — SIN stormflow")
    print(f"{'='*60}")

    tl, vl, tstl = create_dataloaders(
        df_tn_p1, df_vn_p1, df_tsn_p1,
        FEATURES_P1, TARGET_COL, AUX_COL,
        seq_length=72, horizon=h, batch_size=256,
    )

    mdl = TwoStageTCN(n_features=len(FEATURES_P1)).to(device)
    crit = TwoStageLoss(event_threshold=0.5, norm_params=np_p1, cls_weight=0.3, reg_weight=0.7)
    cfg = {'learning_rate': 5e-4, 'weight_decay': 1e-4, 'max_epochs': 100,
           'min_epochs': 0, 'grad_clip_max_norm': 1.0,
           'early_stopping_patience': 10, 'early_stopping_min_delta': 1e-5}

    hist = train_model(mdl, tl, vl, crit, cfg)

    # ─── Guardar pesos ────────────────────────────────────────
    save_name = f"modelo_H{h}_sinSF"

    torch.save(mdl.state_dict(), f"{SAVE_DIR}/{save_name}_weights.pt")

    npar_serializable = {
        'feature_columns': np_p1['feature_columns'],
        'target_col': np_p1['target_col'],
        'log1p_columns': np_p1['log1p_columns'],
        'apply_log1p_to_target': np_p1['apply_log1p_to_target'],
        'mean': {k: float(v) for k, v in np_p1['mean'].items()},
        'std': {k: float(v) for k, v in np_p1['std'].items()},
    }
    with open(f"{SAVE_DIR}/{save_name}_norm_params.json", 'w') as f:
        json.dump(npar_serializable, f, indent=2)

    meta = {
        'horizon': h,
        'stormflow_as_feature': False,
        'n_features': len(FEATURES_P1),
        'features': FEATURES_P1,
        'best_epoch': hist['best_epoch'],
        'seq_length': 72,
        'batch_size': 256,
        'config': cfg,
    }
    with open(f"{SAVE_DIR}/{save_name}_meta.json", 'w') as f:
        json.dump(meta, f, indent=2)

    print(f"  ✓ Checkpoint guardado: {save_name}")

    # ─── Evaluar ──────────────────────────────────────────────
    yp, yr = predict(mdl, tstl, device)

    tc = np_p1['target_col']
    tm = np_p1['mean'][tc]
    ts = np_p1['std'][tc]
    yr_mgd = np.clip(np.expm1(yr * ts + tm), 0, None)
    yp_mgd = np.clip(np.expm1(yp * ts + tm), 0, None)

    ss_res = np.sum((yr_mgd - yp_mgd)**2)
    ss_tot = np.sum((yr_mgd - yr_mgd.mean())**2)
    nse = 1 - ss_res / ss_tot
    rmse = np.sqrt(np.mean((yr_mgd - yp_mgd)**2))
    mae = np.mean(np.abs(yr_mgd - yp_mgd))
    pidx = np.argmax(yr_mgd)
    pr, pp = yr_mgd[pidx], yp_mgd[pidx]
    ep = (pp - pr) / pr * 100
    em = yr_mgd > 50
    be = np.mean(yp_mgd[em] - yr_mgd[em]) if em.sum() > 0 else float('nan')
    bm = yr_mgd < 0.5
    bb = np.mean(yp_mgd[bm] - yr_mgd[bm]) if bm.sum() > 0 else float('nan')

    results_p1.append({'horizon': h, 'minutes': h*5, 'best_epoch': hist['best_epoch'],
                       'nse': nse, 'rmse': rmse, 'mae': mae, 'err_pico': ep,
                       'bias_base': bb, 'bias_extreme': be, 'peak_real': pr, 'peak_pred': pp})

    print(f"  H={h}: NSE={nse:.3f} | RMSE={rmse:.2f} | Pico: {pr:.1f}->{pp:.1f} ({ep:+.1f}%) | Bias extr={be:+.1f}")

    # Limpiar modelo de esta iteración (los datos normalizados se mantienen)
    del mdl, crit, tl, vl, tstl, yp, yr, yp_mgd, yr_mgd
    torch.cuda.empty_cache(); gc.collect()

