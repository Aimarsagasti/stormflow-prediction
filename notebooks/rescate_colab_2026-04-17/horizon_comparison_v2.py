import json

FEATURES_P1 = [
    'rain_in',
    'temp_daily_f', 'api_dynamic',
    'rain_sum_10m', 'rain_sum_15m', 'rain_sum_30m',
    'rain_sum_60m', 'rain_sum_120m', 'rain_sum_180m', 'rain_sum_360m',
    'rain_max_10m', 'rain_max_30m', 'rain_max_60m',
    'minutes_since_last_rain',
    'delta_flow_5m', 'delta_flow_15m',
    'delta_rain_10m', 'delta_rain_30m',
    'hour_sin', 'hour_cos', 'month_sin', 'month_cos',
]
TARGET_COL = 'stormflow_mgd'
AUX_COL = 'is_event'

SAVE_DIR = '/content/drive/MyDrive/Proyecto de capstone/Archivos del proyecto/checkpoints'
os.makedirs(SAVE_DIR, exist_ok=True)

# Split y normalizar
df_train_p1, df_val_p1, df_test_p1 = split_chronological(df_feat)
df_tn_p1, df_vn_p1, df_tsn_p1, np_p1 = normalize_splits(
    df_train_p1, df_val_p1, df_test_p1, FEATURES_P1, TARGET_COL
)
del df_train_p1, df_val_p1, df_test_p1
gc.collect()

for df in [df_tn_p1, df_vn_p1, df_tsn_p1]:
    cols_f64 = df.select_dtypes(include=['float64']).columns
    df[cols_f64] = df[cols_f64].astype('float32')
